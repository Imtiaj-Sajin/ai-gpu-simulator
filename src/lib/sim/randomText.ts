export function randomWords(count: number): string {
  let out = "";
  for (let i = 0; i < count; i++) {
    const w = WORDS[Math.floor(Math.random() * WORDS.length)] ?? "token";
    out += (out ? " " : "") + w;
  }
  return out;
}
